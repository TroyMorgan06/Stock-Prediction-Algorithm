#!/usr/bin/env python3
"""
Create /etc/stock-ai/stock-ai.env with Alpaca credentials (LF-only, chmod 600).

Run on Ubuntu (use venv python so verify works):
  sudo /opt/stock_ai/.venv/bin/python /opt/stock_ai/deploy/setup_alpaca_env.py
  sudo /opt/stock_ai/.venv/bin/python /opt/stock_ai/deploy/setup_alpaca_env.py --from ~/alpaca_paper.env

Optional input file format (two lines, no quotes):
  APCA_API_KEY_ID=PKJJ2WPD7YK2WPJUTZ3OMFKYOR
  APCA_API_SECRET_KEY=4LXYG33xUN4BDzzVyANR5Md8zGAzNMCEWw7P1PRGEBhT

Afterward:
  sudo systemctl daemon-reload
  sudo systemctl restart stock-ai-trade.service
"""

from __future__ import annotations

import argparse
import getpass
import os
import sys
import tempfile


DEFAULT_OUT = "/etc/stock-ai/stock-ai.env"


def _strip_crlf_bom(s: str) -> str:
    s = s.replace("\r\n", "\n").replace("\r", "\n")
    if s.startswith("\ufeff"):
        s = s[1:]
    return s.strip()


def _parse_env_file(text: str) -> tuple[str, str]:
    key_id = ""
    secret = ""
    for raw in _strip_crlf_bom(text).split("\n"):
        line = raw.strip()
        if not line or line.startswith("#"):
            continue
        if "=" not in line:
            continue
        k, v = line.split("=", 1)
        k, v = k.strip(), v.strip().strip('"').strip("'")
        if k in ("APCA_API_KEY_ID", "ALPACA_API_KEY"):
            key_id = v
        elif k in ("APCA_API_SECRET_KEY", "ALPACA_API_SECRET"):
            secret = v
    return key_id.strip(), secret.strip()


def _prompt_pair() -> tuple[str, str]:
    print("Paste Paper keys if using --paper in systemd, or Live keys for live trading.", flush=True)
    key_id = input("APCA_API_KEY_ID: ").strip()
    secret = getpass.getpass("APCA_API_SECRET_KEY (hidden): ").strip()
    return key_id, secret


def _write_env(path: str, key_id: str, secret: str) -> None:
    if not key_id or not secret:
        raise SystemExit("Both APCA_API_KEY_ID and APCA_API_SECRET_KEY must be non-empty.")
    body = (
        "# Generated by setup_alpaca_env.py — edit with care\n"
        f"APCA_API_KEY_ID={key_id}\n"
        f"APCA_API_SECRET_KEY={secret}\n"
    )
    d = os.path.dirname(path) or "."
    os.makedirs(d, mode=0o700, exist_ok=True)
    fd, tmp = tempfile.mkstemp(prefix="stock-ai.env.", dir=d, text=True)
    try:
        with os.fdopen(fd, "w", encoding="utf-8", newline="\n") as f:
            f.write(body)
        os.chmod(tmp, 0o600)
        os.replace(tmp, path)
    finally:
        if os.path.exists(tmp):
            try:
                os.remove(tmp)
            except OSError:
                pass
    os.chmod(path, 0o600)


def _verify(key_id: str, secret: str, *, paper: bool) -> None:
    try:
        from alpaca.trading.client import TradingClient
    except ImportError as exc:
        print(f"Skipping API verify (alpaca-py not installed): {exc}", flush=True)
        return
    print(f"Verifying credentials (paper={paper}) ...", flush=True)
    c = TradingClient(key_id, secret, paper=paper)
    acct = c.get_account()
    print("OK:", acct, flush=True)


def main() -> None:
    ap = argparse.ArgumentParser(description="Write /etc/stock-ai/stock-ai.env for Alpaca.")
    ap.add_argument("--out", default=DEFAULT_OUT, help=f"Output path (default: {DEFAULT_OUT})")
    ap.add_argument("--from", dest="from_file", help="Read KEY=VALUE lines from this file.")
    ap.add_argument(
        "--live",
        action="store_true",
        help="Verify against live API after write (default verifies paper; use Paper keys unless you pass this).",
    )
    ap.add_argument("--no-verify", action="store_true", help="Do not call Alpaca after writing file.")
    args = ap.parse_args()

    if os.geteuid() != 0:
        raise SystemExit(
            "Run with sudo so this can write to /etc/stock-ai/ "
            "(e.g. sudo /opt/stock_ai/.venv/bin/python deploy/setup_alpaca_env.py)"
        )

    if args.from_file:
        with open(args.from_file, "r", encoding="utf-8") as f:
            key_id, secret = _parse_env_file(f.read())
        if not key_id or not secret:
            raise SystemExit(f"Could not parse both keys from {args.from_file!r}. Expected APCA_API_KEY_ID=... and APCA_API_SECRET_KEY=...")
    else:
        key_id, secret = _prompt_pair()

    _write_env(args.out, key_id, secret)
    print(f"Wrote {args.out} (mode 600, LF line endings).", flush=True)

    if args.no_verify:
        return
    paper = not args.live
    try:
        _verify(key_id, secret, paper=paper)
    except Exception as exc:
        print(f"VERIFY FAILED: {exc}", flush=True)
        print("The file was still written. Fix keys or use Paper vs Live to match your dashboard.", flush=True)
        sys.exit(1)


if __name__ == "__main__":
    main()
